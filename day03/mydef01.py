def add(a,b):
    return a+b;

def minus(a,b):
    return a-b;

def multiply(a,b):
    return a*b;

def divide(a,b):
    return a/b;

def remain(a,b):
    return a%b;

sum = add(4,3)
min = minus(4,3)
mul = multiply(4,3)
div = divide(4,3)
re = remain(4,3)

print("sum: ",sum)
print("min: ",min)
print("mul: ",mul)
print("div: ",div)
print("re: ",re)